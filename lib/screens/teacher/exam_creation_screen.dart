  Future<void> _generateExam() async {
    if (_topicsController.text.trim().isEmpty) {
      ScaffoldMessenger.of(context).showSnackBar(
        const SnackBar(content: Text('LÃ¼tfen konu girin')),
      );
      return;
    }

    if (_selectedFormats.isEmpty) {
      ScaffoldMessenger.of(context).showSnackBar(
        const SnackBar(content: Text('En az bir soru formatÄ± seÃ§in')),
      );
      return;
    }

    setState(() {
      _isGenerating = true;
      _generatedVersions = null;
      _selectedVersionIndex = null;
    });

    try {
      final result = await _aiService.generateExam(
        gradeBand: _selectedGrade,
        timeWindow: _selectedTimeWindow,
        topics: _topicsController.text.split(',').map((e) => e.trim()).toList(),
        questionCount: _questionCount,
        durationMinutes: _durationMinutes,
        difficulty: _selectedDifficulty,
        teacherStyle: _selectedTeacherStyle,
        formatsAllowed: _selectedFormats,
      );

      if (mounted) {
        setState(() {
          _generatedVersions = result.versions;
          _summaryForTeacher = result.summaryForTeacher;
          _isGenerating = false;
        });
      }
    } catch (e) {
      if (mounted) {
        setState(() => _isGenerating = false);
        // User-friendly error dialog
        showDialog(
          context: context,
          builder: (ctx) => AlertDialog(
            title: const Text('ÃœzgÃ¼nÃ¼z ðŸ˜”'),
            content: Text(
              e.toString().contains('429') || e.toString().contains('Quota')
                  ? 'Servis ÅŸu an Ã§ok yoÄŸun. LÃ¼tfen 1 dakika sonra tekrar deneyin. (Quota Exceeded)'
                  : 'Bir hata oluÅŸtu: $e',
            ),
            actions: [
              TextButton(
                onPressed: () => Navigator.pop(ctx),
                child: const Text('Tamam'),
              ),
            ],
          ),
        );
      }
    }
  }

  Future<void> _publishExam() async {
    if (_selectedVersionIndex == null || _generatedVersions == null || _currentTeacherId == null) {
      return;
    }

    final selectedVersion = _generatedVersions![_selectedVersionIndex!];

    // Show confirmation dialog with due date picker
    final dueDate = await _showPublishDialog();
    if (dueDate == null) return;

    setState(() => _isLoading = true);

    try {
      final now = DateTime.now();
      
      // Extract answer key from teacher notes
      final teacherNotes = selectedVersion.teacherNotes;
      final answerKey = ExamAnswerKey(
        correctAnswers: Map<String, dynamic>.from(teacherNotes['answerKey'] ?? {}),
        explanations: Map<String, String>.from(teacherNotes['explanations'] ?? {}),
        commonMistakes: (teacherNotes['commonMistakes'] as Map<String, dynamic>?)
            ?.map((k, v) => MapEntry(k, (v as List).cast<String>())) ?? {},
      );

      final exam = Exam(
        id: '', // Will be generated by Firestore
        teacherId: _currentTeacherId!,
        classId: _classContext ?? 'default_class',
        schoolId: 'default_school',
        gradeBand: _selectedGrade,
        timeWindow: _selectedTimeWindow,
        topics: _topicsController.text.split(',').map((e) => e.trim()).toList(),
        teacherStyle: _selectedTeacherStyle,
        status: ExamStatus.draft, // Start as draft
        targetStudentIds: [], // Empty = entire class
        title: selectedVersion.title,
        questions: selectedVersion.questions,
        estimatedMinutes: selectedVersion.estimatedMinutes,
        maxScore: selectedVersion.questions.fold(0, (sum, q) => sum + q.points),
        instructions: selectedVersion.instructions,
        answerKey: answerKey,
        commonMisconceptions: List<String>.from(teacherNotes['commonMisconceptions'] ?? []),
        createdAt: now,
        updatedAt: now,
      );

      // Create draft then publish
      final examId = await _examStore.createDraftExam(exam);
      await _examStore.publishExam(examId: examId, dueDate: dueDate);

      if (mounted) {
        setState(() => _isLoading = false);
        ScaffoldMessenger.of(context).showSnackBar(
          const SnackBar(
            content: Text('âœ… SÄ±nav yayÄ±nlandÄ±!'),
            backgroundColor: Colors.green,
          ),
        );
        Navigator.pop(context); // Return to management screen
      }
    } catch (e) {
      if (mounted) {
        setState(() => _isLoading = false);
        ScaffoldMessenger.of(context).showSnackBar(
          SnackBar(
            content: Text('Hata: $e'),
            backgroundColor: AppTokens.errorLight,
          ),
        );
      }
    }
  }

